package ontology;

public class Spot {

	private Point2D spot;
	private int perc;
	private int parede;
	
	
	public Spot(){
		spot = new Point2D();
		perc = 0;
		parede = 0;
	}
	
	public Spot(Point2D pt, int p, int par){
		this.spot = new Point2D(pt.clone());
		this.perc = p;
		this.parede = par;
	}
	

	public Point2D getSpot() {
		return spot;
	}

	public void setSpot(Point2D spot) {
		this.spot = spot;
	}

	public int getPerc() {
		return perc;
	}

	public void setPerc(int perc) {
		this.perc = perc;
	}

	public int getParede() {
		return parede;
	}

	public void setParede(int parede) {
		this.parede = parede;
	}
	
}